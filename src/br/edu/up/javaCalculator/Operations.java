package br.edu.up.javaCalculator;

public class Operations {
	double addition(double x, double y) {
		return x + y;
	}

	double subtraction(double x, double y) {
		return x - y;
	}

	double multiplication(double x, double y) {
		return x * y;
	}

	double division(double x, double y) {
		return x / y;
	}
}
